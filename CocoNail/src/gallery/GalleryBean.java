package gallery;

public class GalleryBean {
	private int gallery_num;
	private String gallery_img;
	private String gallery_img_name;
	private int gallery_like;
	
	
	public int getGallery_num() {
		return gallery_num;
	}
	public void setGallery_num(int gallery_num) {
		this.gallery_num = gallery_num;
	}
	public String getGallery_img() {
		return gallery_img;
	}
	public void setGallery_img(String gallery_img) {
		this.gallery_img = gallery_img;
	}
	public String getGallery_img_name() {
		return gallery_img_name;
	}
	public void setGallery_img_name(String gallery_img_name) {
		this.gallery_img_name = gallery_img_name;
	}
	public int getGallery_like() {
		return gallery_like;
	}
	public void setGallery_like(int gallery_like) {
		this.gallery_like = gallery_like;
	}

	
	
}
